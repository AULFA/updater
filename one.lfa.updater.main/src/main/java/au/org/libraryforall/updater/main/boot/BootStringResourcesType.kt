package au.org.libraryforall.updater.main.boot

/**
 * The string resources used to describe the boot process.
 */

interface BootStringResourcesType {

  /**
   * Booting started.
   */

  val bootStarted: String

  /**
   * The boot failed with a generic error message.
   */

  val bootFailedGeneric: String

  /**
   * Booting completed.
   */

  val bootCompleted: String

}
